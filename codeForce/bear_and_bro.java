package codeForce;

public class bear_and_bro {
    public static void main(String[] args) {
        int limak=4;
        int bob=9;
        int count=0;
        while(limak<=bob)
        {
            limak*=3;
            bob*=2;
            count++;

        }
        System.out.println(count);
    }
}
